lst = [11, 5, 8, 32, 15, 3, 20, 132, 21, 4, 555, 9, 20]
print("элементы, которые одновременно меньше 30 иделятся на 3 без остатка:")
for i in lst:
    if i < 30 :
        if i % 3 == 0:
            print(i)
            lst.remove(i)
print("Сумма всех остальных элементов:")
c = 0
for b in lst:
    c += b
print(c)
